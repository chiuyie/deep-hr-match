/**
 * Generates lib/constants/geo/world-countries.generated.ts from country-state-city.
 * Run: node scripts/generate-world-countries.mjs
 */
import { Country } from "country-state-city";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.join(__dirname, "..", "lib", "constants", "geo", "world-countries.generated.ts");

const rows = Country.getAllCountries()
  .map((c) => {
    const raw = (c.phonecode ?? "").replace(/\D/g, "");
    const phoneCode = raw ? `+${raw}` : "";
    return {
      name: c.name,
      isoCode: c.isoCode,
      phoneCode,
    };
  })
  .filter((c) => c.name && c.isoCode)
  .sort((a, b) => a.name.localeCompare(b.name));

const body = `/* eslint-disable */
/** Auto-generated by scripts/generate-world-countries.mjs — do not edit manually. */

export type WorldCountryRow = {
  name: string;
  isoCode: string;
  phoneCode: string;
};

export const WORLD_COUNTRIES: WorldCountryRow[] = ${JSON.stringify(rows, null, 2)} as const;

export const WORLD_COUNTRY_NAMES: string[] = [
  ...WORLD_COUNTRIES.map((c) => c.name),
  "Other",
];

export const WORLD_COUNTRY_BY_NAME = new Map(WORLD_COUNTRIES.map((c) => [c.name, c]));
export const WORLD_COUNTRY_BY_ISO = new Map(WORLD_COUNTRIES.map((c) => [c.isoCode, c]));
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, body, "utf8");
console.log(`Wrote ${rows.length} countries to ${outPath}`);
